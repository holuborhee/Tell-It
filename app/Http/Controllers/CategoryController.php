    {
        //
        $cat = Category::findOrFail($id);
        $post = TextPost::where('category_id',$id)->paginate(12);
        if(Auth::check())
            return view('post.allposts',['posts' => $post,'cat'=>$cat] );
        else
            return view('guest.allposts',['posts' => $post,'cat'=>$cat] );
    }

